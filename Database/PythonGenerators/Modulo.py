# Funzione per generare numero di moduli
def num_moduli():
    r = random.random()
    if r < 0.5:
        return 1
    elif r < 0.9:
        return 2
    else:
        return 4

# Funzione per generare date di inizio/fine modulo
def data_modulo(periodo, anno):
    if periodo == 1:
        # metà settembre a metà dicembre
        inizio = datetime(anno, 9, 15)
        fine = datetime(anno, 12, 15)
    else:
        # metà febbraio a metà giugno
        inizio = datetime(anno, 2, 15)
        fine = datetime(anno, 6, 15)
    
    # Spostamento casuale ±7 giorni
    delta_inizio = timedelta(days=random.randint(-7, 7))
    delta_fine = timedelta(days=random.randint(-7, 7))
    
    return inizio + delta_inizio, fine + delta_fine

