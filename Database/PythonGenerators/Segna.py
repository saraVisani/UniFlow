import random

# --- PREPARAZIONE DATI ---
eventi_dict = {e[0]: {'Pubblico': e[6]} for e in eventi}  # {ID_evento: {'Pubblico': 0/1}}

# Collaboratori per evento
collab_dict = {}
for ce, cf in collaboratori:
    collab_dict.setdefault(ce, set()).add(cf)

# Mapping promotore -> eventi che propone
prop_dict = {}
for ce, cp in propongono:
    prop_dict.setdefault(cp, set()).add(ce)

# Mapping evento -> CF dei rappresentanti dei promotori
rapp_dict = {}
for cod_prom, cf in rappresentano:
    for ev in prop_dict.get(cod_prom, set()):
        rapp_dict.setdefault(ev, set()).add(cf)

# CF delle persone nel sistema universitario
sistema_set = set(cf for _, _, _, cf in sistema_universitario)

# --- GENERATORE DI "Segna" ---
segnalista = []

for persona in persone:
    cf_persona = persona[0]
    eventi_possibili = []

    for e in eventi:
        id_evento = e[0]
        pubblico = e[6]

        # 1. Controllo pubblico/privato
        if pubblico == 0 and cf_persona not in sistema_set:
            continue

        # 2. Controllo collaboratore
        if cf_persona in collab_dict.get(id_evento, set()):
            continue

        # 3. Controllo rappresentanti/promotori
        if cf_persona in rapp_dict.get(id_evento, set()):
            continue

        eventi_possibili.append(id_evento)

    # 4. Limitare massimo 3 eventi a persona
    if len(eventi_possibili) > 3:
        eventi_possibili = random.sample(eventi_possibili, 3)

    # 5. Aggiungere alla lista finale
    for id_evento in eventi_possibili:
        segnalista.append((id_evento, cf_persona))

# --- STAMPA COME INSERT SQL ---
for id_evento, cf in segnalista:
    print(f'insert into Segna values ({id_evento}, "{cf}");')
